function toFlagEmoji(alpha2Code) {
  const [c1, c2] = alpha2Code;
  return String.fromCodePoint(
    c1.codePointAt(0) + 0x1F1E6 - 0x41,
    c2.codePointAt(0) + 0x1F1E6 - 0x41);
}

function countryByCode(alpha2code) {
  // https://www.iban.com/country-codes
  return {
    alpha2code,
    flag: toFlagEmoji(alpha2code),
    ...countries[alpha2code]
  };
}

async function processImageFile(file, options = { estimateColor: true }) {
  const imageFile = await processFile(file);

  if (options.estimateColor) {
    imageFile.color = await estimateImageColor(imageFile.url);
  }

  return imageFile;
}

async function processFile(file, decoder) {

  if (!file) return null;

  return new Promise((resolve, reject) => {
    try {
      const reader = new FileReader();
      reader.readAsDataURL(file);
      reader.onerror = reject;
      reader.onload = async () => {
        const url = reader.result;
        const result = {
          url,
          data: url
        };

        if (decoder) {
          result.data = await decoder(url);
        }

        resolve(result);
      };
    } catch (err) {
      reject(err);
    }
  });
}

function estimateImageColor(url) {
  return new Promise(resolve => {
    const context = document.createElement('canvas').getContext('2d');
    const img = new Image();
    img.src = url;
    img.onload = function () {
      context.drawImage(img, 0, 0, 1, 1);
      let [r, g, b] = context.getImageData(0, 0, 1, 1).data.slice(0, 3);
      r = Math.round(r).toString(16).padStart(2, '0');
      g = Math.round(g).toString(16).padStart(2, '0');
      b = Math.round(b).toString(16).padStart(2, '0');
      resolve('#' + r + g + b);
    };
  });
}

